class Book:
    def __init__(self, id_: int, name: str, pages: int):
        self.id = id_
        self.name = name
        self.pages = pages

    def __str__(self) -> str:
        """
        Returns the format string Book "book title"
        """
        true_format = f'Книга "{self.name}"'
        return true_format

    def __repr__(self) -> str:
        """
        Returns a valid python string
        from which exactly the same instance can be initialized
        """
        valid_string = f"Book(id_={self.id}, name='{self.name}', pages={self.pages})"
        return valid_string


